package cmm01;

public class No04_ComparisionDemo {
	public static void main(String[] args) {
	int val01 = 1;
	int val02 = 2;
	
	
	if(val01==val02 ){
			System.out.println("val01==val02 ");
		
	}
	if(val01!=val02){
		
		System.out.println("val01 != val02 ");
	}
	if(val01>val02){
		System.out.println("val01>val02 ");
			
	}
		
	if(val01<val02){
		System.out.println("val01  < val02");
		
	}
	//조건문이라고 한다.
	}
	
	
}
